package com.talesapp.phonereceiver.common;

import android.graphics.drawable.Drawable;

public class Contacts {

	private String phone;
	private String name;
	private Drawable proFilePic;
	private Drawable bgImg;
	private String desc;
	private String url;
	private String profilePicPath;
	private String bgImgPath;
	private String profilePicUrl;
	private String bgImgUrl;
	
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Drawable getProfilePic() {
		return proFilePic;
	}
	public void setProFilePic(Drawable proFilePic) {
		this.proFilePic = proFilePic;
	}
	public Drawable getBgImg() {
		return bgImg;
	}
	public void setBgImg(Drawable bgImg) {
		this.bgImg = bgImg;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getProfilePicPath() {
		return profilePicPath;
	}
	public void setProfilePicPath(String profilePicPath) {
		this.profilePicPath = profilePicPath;
	}
	public String getBgImgPath() {
		return bgImgPath;
	}
	public void setBgImgPath(String bgImgPath) {
		this.bgImgPath = bgImgPath;
	}
	public String getProfilePicUrl() {
		return profilePicUrl;
	}
	public void setProfilePicUrl(String profilePicUrl) {
		this.profilePicUrl = profilePicUrl;
	}
	public String getBgImgUrl() {
		return bgImgUrl;
	}
	public void setBgImgUrl(String bgImgUrl) {
		this.bgImgUrl = bgImgUrl;
	}
	
	
}
